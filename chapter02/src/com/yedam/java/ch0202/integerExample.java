package com.yedam.java.ch0202;

public class integerExample {

	public static void main(String[] args) {
		// byte : 1byte = 8bit
		byte v1 = -128;
		byte v2 = -30;
		byte v3 = 0;
		byte v4 = 30;
		// byte v5 = 128; // byte의 범위 : -128 ~ 127
		
		// int : 4byte = Default
		int var1 = 1000000000;
		// long : 8byte => 리터럴로 long타입 범위 안의 값을 쓸 경우 L(대문자)을 붙임
		long var2 = 10000000000L;

	}

}
